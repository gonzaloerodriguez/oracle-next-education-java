public class EjemploCaracteresString {
    public static void main(String[] args) {

        // tipo de dato char acepta un solo caracter y se escribe con comillas simples
        // ''

        char caracter = 'a';

        System.out.println(caracter);

        // tambien se puede poner el caracter en codigo ASCII

        caracter = 97;

        System.out.println(caracter);

        // tipo de dato String acepta una cadena de caracteres y se escribe con comillas
        // dobles ""

        String palabra = "Aprendiendo Java";

        System.out.println(palabra);

    }
}
